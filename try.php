        <dt class="tab third"><span>FAQs</span></dt>
        <dd class="tab-container third">
            <div class="tab-content">
                <div class="faqs">
                    <div class="block-wrapper">
                        <div class="block">
                            <div class="block-title"><strong><span class="highlight">Why does the PowerControl have keypad program selection buttons?</span></strong></div>
                            <div class="content-wrapper">
                                <div class="block-content">
                                    <p>Each DTE performance tuning module has three different programs which are fully adjustable<sup>1</sup>: Sport, Dynamic and Efficiency. The driver can select a program at the simple touch of a button.</p>
                                    <p style="font-size: 11px;"><sup>1</sup>Deactivation required for TÜV approval.</p>
                                </div>
                            </div>
                        </div>
                    </div>
                    <div class="block-wrapper">
                        <div class="block">
                            <div class="block-title"><strong><span class="highlight">Is the tuning system detectable?</span></strong></div>
                            <div class="content-wrapper">
                                <div class="block-content">
                                    <p>No, after the harness and module have been removed, the vehicle is back to its original stock condition and the tuning system is not detectable. DTE’s PowerControl does not over-ride any engine protective functions and optimises all performance increases within the ECU’s standard working range, therefore no messages are logged in its memory.</p>
                                </div>
                            </div>
                        </div>
                    </div>
                    <div class="block-wrapper">
                        <div class="block">
                            <div class="block-title"><strong><span class="highlight">Do I have to register my performance increase with the TÜV, the Dekra etc? (Europe only)</span></strong></div>
                            <div class="content-wrapper">
                                <div class="block-content">
                                    <p>Yes, an increase in performance needs to be registered, since the installation of an additional control system represents a structural change. Without entry thereof into the vehicles documents by means of partial inspections or individual inspection via a test report, may lead to the loss of insurance cover and the cancellation of the road traffic operating license.</p>
                                </div>
                            </div>
                        </div>
                    </div>
                    <div class="block-wrapper">
                        <div class="block">
                            <div class="block-title"><strong><span class="highlight">Where is the performance system connected?</span></strong></div>
                            <div class="content-wrapper">
                                <div class="block-content">
                                    <p>The performance system is connected to some of the main engine sensors, which may include fuel injection, boost pressure sensors, rail pressure sensors (for diesel engines) and intake manifold pressure sensors (for petrol engines) as well as the camshaft’s connections. The relevant signals are assessed, optimised and then sent on to the engine which improves its performance.</p>
                                </div>
                            </div>
                        </div>
                    </div>
                    <div class="block-wrapper">
                        <div class="block">
                            <div class="block-title"><strong><span class="highlight">Is it possible to increase performance and also save on my fuel bill?</span></strong></div>
                            <div class="content-wrapper">
                                <div class="block-content">
                                    <p>Yes, thanks to the engines improved efficiency, fuel consumption can be reduced by up to 15%. By changing gears earlier as well as more frequent driving in the lower rev range, fuel savings are most certainly possible.</p>
                                </div>
                            </div>
                        </div>
                    </div>
                    <div class="block-wrapper">
                        <div class="block">
                            <div class="block-title"><strong><span class="highlight">How long does the installation of the PowerControl take?</span></strong></div>
                            <div class="content-wrapper">
                                <div class="block-content">
                                    <p>Our performance enhancements are delivered ready-to-install. A detailed and illustrated step-by-step guide facilitates installation. Clearly labelled components and wiring also make things really easy. This makes the installation of the PowerControl really straight forward and, depending on the vehicles make and model, usually takes no longer than 30 minutes.</p>
                                </div>
                            </div>
                        </div>
                    </div>
                    <div class="block-wrapper">
                        <div class="block">
                            <div class="block-title"><strong><span class="highlight">Can I transfer my PowerControl to my new vehicle?</span></strong></div>
                            <div class="content-wrapper">
                                <div class="block-content">
                                    <p>100%, with a quick reprogram of your existing module and possibly a harness change, you can transfer your PowerControl to your new vehicle, and at a fraction of the cost of a new system. You can send it in for us take care of the conversion for you or visit our HQ and let us do the rest. Please note: The new engines technology should be compatible with that of the current systems.</p>
                                </div>
                            </div>
                        </div>
                    </div>
                    <div class="block-wrapper">
                        <div class="block">
                            <div class="block-title"><strong><span class="highlight">Do I need to report my insurance?</span></strong></div>
                            <div class="content-wrapper">
                                <div class="block-content">
                                    <p>Yes, please inform your insurance company about the vehicle’s performance changes. (Only in Europe)</p>
                                </div>
                            </div>
                        </div>
                    </div>
                    <div class="block-wrapper">
                        <div class="block">
                            <div class="block-title"><strong><span class="highlight">What about my vehicle warranty?</span></strong></div>
                            <div class="content-wrapper">
                                <div class="block-content">
                                    <p>An increase in performance may lead to the exclusion of warranty claims against the seller and any warranty claims against the manufacturer. You will receive a comprehensive guarantee package from DTE Systems. (Only in Europe, coming soon to Southern Africa)</p>
                                </div>
                            </div>
                        </div>
                    </div>
                    <div class="block-wrapper">
                        <div class="block">
                            <div class="block-title"><strong><span class="highlight">Can I still wash my engine?</span></strong></div>
                            <div class="content-wrapper">
                                <div class="block-content">
                                    <p>Yes, the all-over epoxy resin coating of DTE’s high performance tuning PC boards ensures excellent durability and the best possible protection against moisture.</p>
                                </div>
                            </div>
                        </div>
                    </div>
                    <div class="block-wrapper">
                        <div class="block">
                            <div class="block-title"><strong><span class="highlight">What is EngineProtect +?</span></strong></div>
                            <div class="content-wrapper">
                                <div class="block-content">
                                    <p>Under Engine Protect +, DTE introduces a brand new series of industry leading security standards. These continuously monitor engine signals and protect all the connected sensors which guards against ECU overload.</p>
                                </div>
                            </div>
                        </div>
                    </div>
                    <div class="block-wrapper">
                        <div class="block">
                            <div class="block-title"><strong><span class="highlight">Do the engines emission values remain unaffected?</span></strong></div>
                            <div class="content-wrapper">
                                <div class="block-content">
                                    <p>Yes, a major advantage of DTE’s current auxiliary control unit, is its compliance with the statutory and vehicle emission values. The engines combustion is improves in all operational areas which means that the fuel is used 100% efficiently. This also means that DTE can increase the performance of vehicles which aren’t fitted with diesel particle filters without any concerns. Compliance with the engines exhaust gas values is confirmed by the vehicle-specific parts certificate.</p>
                                </div>
                            </div>
                        </div>
                    </div>
                    <div class="block-wrapper">
                        <div class="block">
                            <div class="block-title"><strong><span class="highlight">Does the ECU’s protection functions remain intact?</span></strong></div>
                            <div class="content-wrapper">
                                <div class="block-content">
                                    <p>Yes, the protective functions of the engine management system are always maintained and the vehicle is fully diagnosable.</p>
                                </div>
                            </div>
                        </div>
                    </div>
                    <div class="block-wrapper">
                        <div class="block">
                            <div class="block-title"><strong><span class="highlight">My vehicle already has high mileage. Will I still benefit from an increase in performance?</span></strong></div>
                            <div class="content-wrapper">
                                <div class="block-content">
                                    <p>If a vehicle has been well maintained and serviced regularly, or even if it runs well but has high mileage, still makes it a perfect candidate for our performance enhancements.</p>
                                </div>
                            </div>
                        </div>
                    </div>
                    <div class="block-wrapper">
                        <div class="block">
                            <div class="block-title"><strong><span class="highlight">Do you offer chip tuning for motorcycles?</span></strong></div>
                            <div class="content-wrapper">
                                <div class="block-content">
                                    <p>No, as a manufacturer of performance enhancements, our focus is on the optimisation of motor vehicles, commercial vehicles, tractors and boats.</p>
                                </div>
                            </div>
                        </div>
                    </div>
                    <div class="block-wrapper">
                        <div class="block">
                            <div class="block-title"><strong><span class="highlight">Does the increase in performance have an impact on the vehicle’s tax? </span></strong></div>
                            <div class="content-wrapper">
                                <div class="block-content">
                                    <p>No, due to the fact that vehicle’s tax is determined by the engines displacement and its relevant pollutant class, which remain unchanged by the DTE’s performance systems, means that the vehicle remains in its relevant tax bracket.</p>
                                </div>
                            </div>
                        </div>
                    </div>
                    <div class="block-wrapper">
                        <div class="block">
                            <div class="block-title"><strong><span class="highlight">My car has an automatic gearbox, will my vehicles performance still increase?</span></strong></div>
                            <div class="content-wrapper">
                                <div class="block-content">
                                    <p>Yes of course, DTE’s PowerControl can also be used on vehicles with automatic gearboxes, dual clutch transmissions (e.g., DSG), or semi-automatic (e.g., tiptronic).</p>
                                </div>
                            </div>
                        </div>
                    </div>
                    <div class="block-wrapper">
                        <div class="block">
                            <div class="block-title"><strong><span class="highlight">Is it possible for my car to smoke excessively?</span></strong></div>
                            <div class="content-wrapper">
                                <div class="block-content">
                                    <p>No, DTE’s PowerControl optimizes the air &amp; fuel ratio and not only the amount of fuel injected, but also the quantity thereof as well as the AFR’s (air to fuel ratio’s). This ensures a balanced air to fuel ratio throughout the entire RPM range as well as very low smoke emissions. Compliance with emission values are also certified within our parts certificates.</p>
                                </div>
                            </div>
                        </div>
                    </div>
